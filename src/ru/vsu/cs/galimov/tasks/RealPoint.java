package ru.vsu.cs.galimov.tasks;

public class RealPoint {
    private final double x;
    private final double y;

    public RealPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
